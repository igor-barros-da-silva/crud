    ArrayList<Integer> idPessoas = new ArrayList<>();
    ArrayList<Integer> idCidades = new ArrayList<>();
    
    private void atualizarListaPessoas() {
        try {
            Connection conn = Banco.abrirConexao();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM pessoa");
            ResultSet rs = ps.executeQuery();            
            String cidades = "";
            while (rs.next()) {
                cidades += rs.getInt("id") + " - ";                
                cidades += rs.getString("nome") + "\n";
            }
            painelPessoas.setText(cidades);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void preencherpainelPessoas() {
        try {
            Connection conn = Banco.abrirConexao();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM pessoa");
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                idPessoas.add(rs.getInt("id"));
            }
            
            ps.close();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void preencherComboCidades() {
        try {
            Connection conn = Banco.abrirConexao();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM cidade");
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                idCidades.add(rs.getInt("id"));
                comboCidade.addItem(rs.getString("nome"));
            }
            
            ps.close();
            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
