    private void botaoIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoIncluirActionPerformed
        Connection conexao = Banco.abrirConexao();
        try {
            PreparedStatement comando = conexao.prepareStatement("insert into pessoa (nome, idade, rg, cpf, idcidade) values (?, ?, ?, ?, ?)");
            comando.setString(1, campoNome.getText());
            comando.setString(2, campoIdade.getText());
            comando.setString(3, campoRg.getText());
            comando.setString(4, campoCpf.getText());
            comando.setInt(5, idCidades.get(comboCidade.getSelectedIndex()));
            comando.executeUpdate();
            comando.close();
            conexao.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        atualizarListaPessoas();
    }//GEN-LAST:event_botaoIncluirActionPerformed

